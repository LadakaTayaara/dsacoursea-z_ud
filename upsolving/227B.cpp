// Macros
#define pb push_back
#define all(x) (x).begin(), (x).end()
#define rall(x) (x).rbegin(), (x).rend()

void solve() {
    int a,b,x,y;
    cin>>a>>b>>x>>y;
    while(a-x >= 0){
        b+=y;
        a-=x;
    }
    cout<<a+b<<"\n";
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}